export function sampleReceivedAt(index: number, total: number): string {
  const now = Date.now();
  const windowMs = 2 * 60 * 60 * 1000;
  const offset = Math.round((windowMs * (total - index)) / total);
  return new Date(now - offset).toISOString();
}
